package com.thinkify.CabBookingApp.Model;

public class Ride {
	String username;
	long user_sourcex;
	long user_sourcey;
	long user_destx;
	long user_desty;

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public long getUser_sourcex() {
		return user_sourcex;
	}

	public void setUser_sourcex(long user_sourcex) {
		this.user_sourcex = user_sourcex;
	}

	public long getUser_sourcey() {
		return user_sourcey;
	}

	public void setUser_sourcey(long user_sourcey) {
		this.user_sourcey = user_sourcey;
	}

	public long getUser_destx() {
		return user_destx;
	}

	public void setUser_destx(long user_destx) {
		this.user_destx = user_destx;
	}

	public long getUser_desty() {
		return user_desty;
	}

	public void setUser_desty(long user_desty) {
		this.user_desty = user_desty;
	}
   
}
